matn = input('Enter a string: ')

# Print the length of the string
print(f'Length of the string: {len(matn)}')

# Convert the string to uppercase
uppercase_matn = matn.upper()
print(f'Uppercase: {uppercase_matn}')

# Convert the string to lowercase
lowercase_matn = matn.lower()
print(f'Lowercase: {lowercase_matn}')